package Java;

public class ForLoop {

	public static void main(String[] args) {
  
		//the loop is for loop
    
		for (int i=0;i<=10;i++) {
			
      // the loop continues to process unitl it meets its condition
      
			System.out.println(i);
      
      // prints the variable i as it increments by 1
      
		}

	}

}
